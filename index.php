                <h3 style="text-align: center;">ISLA COIBA, PANAMÁ</h3>
                <p >
                    El Parque Nacional Coiba es una reserva natural situada en el archipiélago homónimo perteneciente a los distritos de Montijo y
                    de Soná de la provincia de Veraguas, a 25 minutos de vuelo desde la Ciudad de Panamá. Fue declarado como Patrimonio de la Humanidad 
                    por la UNESCO en el año 2005. La isla de Coiba cuenta con 503 km² siendo la más extensa de la geografía panameña.
                </p>
                <p >
                    El área contiene los últimos bosques húmedos y muy húmedos de tierras bajas inalterados de la vertiente Pacífica de la República de Panamá 
                    y contiene, igualmente, formaciones vegetales menores de gran interés para la conservación de su biodiversidad, entre las que resaltan: 
                    los alcornocales (Mora oleifera), sangrillales (Pterocarpus officinalis), la vegetación de playas, el bosque seco costero y herbazales y 
                    arbustales. Además, existen extensiones considerables de manglares, tales como, el mangle rojo (Rhizophora mangle), mangle caballero 
                    (Rhizophora racemosa) y mangle piñuelo (Pelliciera rhizophorae).
                    Se reportan 39 especies de mamíferos, pero no hay registros de grandes depredadores, tales como los felinos. Hay una especie endémica de 
                    mamíferos: el ñeque de Coiba (Dasyprocta coibae) y tres subespecies: la zarigüeya (Didelphis marsupiales battyi), el mono aullador de Coiba 
                    (Alouatta palliata coibensis), que está entre los monos más grandes del Nuevo Mundo, y un ciervo (Odocoileus virginianus rotschildi). Coiba 
                    es el único sitio de Panamá en el que actualmente se pueden observar bandadas de las guacamayas rojas (Ara macao), especie amenazada a nivel 
                    nacional. 
                </p>
